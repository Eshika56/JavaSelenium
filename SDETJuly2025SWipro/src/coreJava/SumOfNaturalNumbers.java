package coreJava;



public class SumOfNaturalNumbers {

    public static void main(String[] args) {
       

        
        System.out.print("Enter the value of N: ");
        int N = 5;
        
       
        

        int sum = 0;
        int i = 1;

        
        while (i <= sum) {
            sum += i;
            i++;
        }

        
        System.out.println("Sum of first " + N + " natural numbers is: " + sum);

        
}
}
